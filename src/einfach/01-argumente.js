/**
 * Dieses Funktion addiert zwei Argumente
 *
 * @param int a
 * @param int b
 *
 * @returns int
 */
export function plus(a, b) {
  return a + a;
}

/**
 * Dieses Funktion subtrahiert zwei Argumente
 *
 * @param int a
 * @param int b
 *
 * @returns int
 */
export function minus(a) {
  return a - b;
}
